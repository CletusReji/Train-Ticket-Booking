    public showtrains() {
        initComponents();
        train cur=global.bkdetails.traveldate.trnlink.first;
        DefaultTableModel model=(DefaultTableModel)table_trains.getModel();
        model.setRowCount(0);
        while(cur!=null) {
            if(cur.priority<=global.bkdetails.srcstation.priority&&cur.priority<=global.bkdetails.deststation.priority&&cur.direction==global.bkdetails.direction) {
                int trndist;
                if(cur.direction=='n') {
                    trndist=global.bkdetails.srcstation.src_distance-cur.start.src_distance;
                }
                else {
                    trndist=(global.bkdetails.srcstation.src_distance-cur.start.src_distance)*(-1);
                }
                global.bkdetails.arrtime=cur.starttime+(trndist/60)*100+(trndist%60);
                if(global.bkdetails.direction=='n') {
                    global.bkdetails.distance=global.bkdetails.deststation.src_distance-global.bkdetails.srcstation.src_distance;
                }
                else {
                    global.bkdetails.distance=global.bkdetails.srcstation.src_distance-global.bkdetails.deststation.src_distance;
                }
                int seata=0;
                String ctype=global.bkdetails.coachtype;
                switch (ctype) {
                    case "SL":
                        seata=cur.seatavailable(cur.sleeper.type, global.bkdetails.quota);
                        global.bkdetails.price=100;
                        if(global.bkdetails.distance>5) {
                            global.bkdetails.price+=(global.bkdetails.distance-5)*5;
                        }
                        break;
                    case "3AC":
                        seata=cur.seatavailable(cur.threeac.type, global.bkdetails.quota);
                        global.bkdetails.price=500;
                        if(global.bkdetails.distance>10) {
                            global.bkdetails.price+=(global.bkdetails.distance-10)*15;
                        }
                        break;
                    case "CC":
                        seata=cur.seatavailable(cur.chaircar.type, global.bkdetails.quota);
                        global.bkdetails.price=250;
                        if(global.bkdetails.distance>8) {
                            global.bkdetails.price+=(global.bkdetails.distance-8)*10;
                        }
                        break;
                    default:
                        break;
                }
                int trainpr=cur.priority;
                switch(trainpr) {
                    case 2:
                        global.bkdetails.price+=10;
                        break;
                    case 3:
                        global.bkdetails.price+=25;
                        break;
                    default:
                        break;
                }
                model.addRow(new Object[] {
                cur.number, cur.name, global.bkdetails.arrtime, seata, global.bkdetails.price});
            }
            cur=cur.next;
        }
    }

