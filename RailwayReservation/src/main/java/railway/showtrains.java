    private void table_trainsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_trainsMouseClicked
        int row=table_trains.getSelectedRow();
        Object tnoobj=table_trains.getValueAt(row, 0);
        int tno=(int) tnoobj;
        train cur=global.bkdetails.traveldate.trnlink.first;
        while(cur!=null) {
            if(cur.number==tno) {
                global.bkdetails.slcttrain=cur;
                break;
            }
            cur=cur.next;
        }
        this.hide();
        bookpassenger bkpsgr=new bookpassenger();
        bkpsgr.setVisible(true);
    }//GEN-LAST:event_table_trainsMouseClicked

